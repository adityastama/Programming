#include <iostream>
using namespace std;

int main()
{
	// while
	// adalah pengulangan perintah
	//	jika syaratnya terpenuhi / benar (true)

	int a = 1;

	while ( a <= 10 )
	{
		cout << a << endl;
		a += 1; // ini agar loop berhenti
				// bisa pakai assigment seperti di atas 
	} 

	cout << "selesai" << endl;

	return 0;
}
